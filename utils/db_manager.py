# =========================================================
# IMPORTS
# =========================================================

import duckdb

from pathlib import Path

# =========================================================
# BASE DIRECTORY
# =========================================================

BASE_DIR = Path(__file__).resolve().parent.parent

# =========================================================
# DATABASE DIRECTORY
# =========================================================

DATABASE_DIR = (
    BASE_DIR
    / "database"
)

DATABASE_DIR.mkdir(
    exist_ok=True
)

# =========================================================
# DATABASE FILE
# =========================================================

DB_FILE = (
    DATABASE_DIR
    / "institutional_quant.db"
)

# =========================================================
# GET CONNECTION
# =========================================================

def get_connection():

    return duckdb.connect(
        str(DB_FILE)
    )
